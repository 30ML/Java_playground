public class Calc2 {
  public static void main(String[] args) {
    int a = 10;

    System.out.println(" a = " + a);
    System.out.println(" a += 10, " + (a += 10));
    System.out.println(" a -= 10, " + (a -= 10));
    System.out.println(" a *= 10, " + (a *= 10));
    System.out.println(" a /= 10, " + (a /= 10));
    System.out.println(" a %= 10, " + (a %= 10));
  }
}
