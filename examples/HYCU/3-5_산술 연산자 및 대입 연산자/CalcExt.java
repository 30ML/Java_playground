public class CalcExt {
  public static void main(String[] args) {
    System.out.println("Math.pow(2, 3) = " + Math.pow(2, 3));
    System.out.println("Math.pow(2, -2) = " + Math.pow(2, -2));
    System.out.println("Math.sqrt(16) = " + Math.sqrt(16));
    System.out.println("Math.cbrt(8) = " + Math.cbrt(8));
    System.out.println("Math.pow(16, 1/4.0) = " + Math.pow(16, 1 / 4.0));
    System.out.println("Math.abs(-4) = " + Math.abs(-4));
  }
}
